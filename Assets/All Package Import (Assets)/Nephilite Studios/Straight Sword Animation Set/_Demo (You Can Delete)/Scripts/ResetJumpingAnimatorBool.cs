    public class ResetJumpingAnimatorBool : StateMachineBehaviour
    {
        AnimationDemoController controller;

        // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
        override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
        {
            if (controller == null)
            {
                controller = animator.GetComponent<AnimationDemoController>();
            }

            controller.isUsingRootMotion = true;
            controller.ResetIsJumping();
        }

